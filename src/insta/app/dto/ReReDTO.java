package insta.app.dto;

public class ReReDTO {
	private int re_re_num;
	private int reply_num;
	private String re_re_date;
	private String re_re_contents;
	private String re_re_Id;
	private int board_num;
	
	public ReReDTO() {}

	public int getRe_re_num() {
		return re_re_num;
	}

	public void setRe_re_num(int re_re_num) {
		this.re_re_num = re_re_num;
	}

	public int getReply_num() {
		return reply_num;
	}

	public void setReply_num(int reply_num) {
		this.reply_num = reply_num;
	}

	public String getRe_re_date() {
		return re_re_date;
	}

	public void setRe_re_date(String re_re_date) {
		this.re_re_date = re_re_date;
	}

	public String getRe_re_contents() {
		return re_re_contents;
	}

	public void setRe_re_contents(String re_re_contents) {
		this.re_re_contents = re_re_contents;
	}

	public String getRe_re_Id() {
		return re_re_Id;
	}

	public void setRe_re_Id(String re_re_Id) {
		this.re_re_Id = re_re_Id;
	}

	public int getBoard_num() {
		return board_num;
	}

	public void setBoard_num(int board_num) {
		this.board_num = board_num;
	}
}
